import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'
import { readFile } from 'node:fs/promises'

Object.defineProperty(process.versions, 'node', {
  value: '18.9.0',
})

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')
const uiIndexPath = resolve(root, '../../packages/ui/dist/index.js')
const command = process.argv[2]
const { build, createServer, serve } = await import('vitepress')

const sleep = (ms) => new Promise((resolvePromise) => setTimeout(resolvePromise, ms))

async function waitForUiDist(timeoutMs = 30_000) {
  const startedAt = Date.now()
  while (Date.now() - startedAt < timeoutMs) {
    try {
      const code = await readFile(uiIndexPath, 'utf8')
      if (code.includes('SunnyUIPlugin')) {
        return
      }
    } catch {
      // Keep polling until dist is generated by @sunny/ui dev.
    }
    await sleep(250)
  }
  throw new Error(`Timed out waiting for UI dist entry: ${uiIndexPath}`)
}

if (command === 'build') {
  await build(root, {
    onAfterConfigResolve(siteConfig) {
      siteConfig.site.title = 'Sunny UI'
      siteConfig.site.description =
        'Sunny UI documentation for the Sunny monorepo, powered by VitePress.'
    },
  })
} else if (command === 'dev') {
  await waitForUiDist()
  const server = await createServer(root, {
    host: '127.0.0.1',
    port: 4174,
  })

  await server.listen()
  server.printUrls()
} else if (command === 'preview') {
  await serve({
    root,
    host: '127.0.0.1',
    port: 4175,
  })
} else {
  throw new Error(`Unknown command: ${command}`)
}
